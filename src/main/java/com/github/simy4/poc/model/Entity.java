package com.github.simy4.poc.model;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBIgnore;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBRangeKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTypeConverted;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBVersioned;
import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.databind.annotation.JsonDeserialize;
import com.github.simy4.poc.model.converters.DynamoDBTypeConvertedInstant;
import com.github.simy4.poc.model.converters.DynamoDBTypeConverterIso;
import com.github.simy4.poc.model.generators.DynamoDBAutoGeneratedInstant;
import org.immutables.value.Value;
import org.springframework.lang.Nullable;

import java.time.Instant;
import java.util.List;

@Data
@Value.Immutable
@Value.Modifiable
@DynamoDBTable(tableName = "${db.entities.table-name}")
@DynamoDBTypeConverted(converter = Entity.Converter.class)
@JsonDeserialize(as = ImmutableEntity.class)
public abstract class Entity {
  public static final String PK_PREFIX = "Entity#";

  public static Identity id(String pk, String sk) {
    return new Identity(PK_PREFIX + pk, sk);
  }

  @Value.Auxiliary
  @JsonIgnore
  @DynamoDBIgnore
  public Identity getId() {
    return id(getPk(), getSk());
  }

  public abstract String getTenant();

  public abstract String getName();

  public abstract Address getAddress();

  public abstract List<Email> getEmails();

  public abstract Status getStatus();

  @Value.Default
  @JsonIgnore
  @DynamoDBHashKey
  public String getPk() {
    return PK_PREFIX + getTenant();
  }

  @Nullable @JsonProperty("id")
  @DynamoDBRangeKey
  @DynamoDBAutoGeneratedKey
  public abstract String getSk();

  @Nullable @DynamoDBAutoGeneratedInstant
  @DynamoDBTypeConvertedInstant
  public abstract Instant getUpdated();

  @Value.Default
  @DynamoDBTypeConvertedInstant
  public Instant getCreated() {
    return Instant.now();
  }

  @Nullable @JsonIgnore
  @DynamoDBVersioned
  public abstract Long getVersion();

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null || getClass() != obj.getClass()) {
      return false;
    }
    Entity entity = (Entity) obj;
    return getId().equals(entity.getId());
  }

  @Override
  public int hashCode() {
    return getId().hashCode();
  }

  public static final class Converter
      extends DynamoDBTypeConverterIso<ModifiableEntity, ImmutableEntity> {
    public Converter() {
      super(entity -> new ModifiableEntity().from(entity), ModifiableEntity::toImmutable);
    }
  }
}
